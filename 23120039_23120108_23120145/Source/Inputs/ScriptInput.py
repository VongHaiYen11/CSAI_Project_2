def write_input_file(filename, matrix):
    """Ghi matrix thành file với format CSV"""
    with open(f"Inputs/{filename}", 'w') as f:
        for row in matrix:
            line = ','.join(map(str, row))
            f.write(line + '\n')

# Tạo tất cả các file
for filename, matrix in test_cases.items():
    write_input_file(filename, matrix)
